    private void inicializaEditorPanes()
    {
        
        this.setTitle(language.getProperty("cnxmatricula.plandocente.title") + grupo.getNombreActividad()); //NOI18N
        jEdtPanePlanDocente.setContentType("text/html"); 
        jEdtPaneInfoOnline.setContentType("text/html"); 
//        jEdtPanePlanDocente.setText(grupo.getPlanDocenteCurso());
        jEdtPanePlanDocente.addHyperlinkListener(this);
        jEdtPaneInfoOnline.addHyperlinkListener(this);
        tabPlanDocente.setTitleAt(0, language.getProperty("cnxmatricula.plandocente.tabplandocente"));
        tabPlanDocente.setTitleAt(1, language.getProperty("cnxmatricula.plandocente.tabinfoonline"));
        tabPlanDocente.setToolTipText(null);
        tabPlanDocente.setToolTipTextAt(0, language.getProperty("cnxmatricula.plandocente.tooltipinternet")); 
        tabPlanDocente.setToolTipTextAt(1, language.getProperty("cnxmatricula.plandocente.tooltipinternet2"));
        jEdtPanePlanDocente.setCaretPosition(0);
        
        try {
            jEdtPaneInfoOnline.setPage("http://es.wikipedia.org/w/index.php?search=" + 
                                        grupo.getNombreActividad().replaceAll("\\s", "+"));                                    
        }
        catch (IOException ex) {
            
            //NOI18N
            JOptionPane.showMessageDialog(null, 
                language.getProperty("err.generic") + "\n" + language.getProperty("err.detail") + ":\n\n" + ex.getMessage(), 
                language.getProperty("app.title"), 
                JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception ex) 
        {
             JOptionPane.showMessageDialog(null, 
                language.getProperty("err.generic") + "\n" + language.getProperty("err.detail") + ":\n\n" + ex.getMessage(), 
                language.getProperty("app.title"), 
                JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    @Override
    public void hyperlinkUpdate(HyperlinkEvent event) {
        if (event.getEventType() == HyperlinkEvent.EventType.ACTIVATED) {
            try {
                
                if (tabPlanDocente.getSelectedIndex() == 0)
                    jEdtPanePlanDocente.setPage(event.getURL());
                else
                    jEdtPaneInfoOnline.setPage(event.getURL());
                
            }catch (IOException ex) {
                
                //NOI18N
                JOptionPane.showMessageDialog(null, 
                    language.getProperty("err.generic") + "\n" + language.getProperty("err.detail") + ":\n\n" + ex.getMessage(), 
                    language.getProperty("app.title"), 
                    JOptionPane.ERROR_MESSAGE);
                
            }catch (Exception ex)
            {
                //NOI18N
                JOptionPane.showMessageDialog(null, 
                    language.getProperty("err.generic") + "\n" + language.getProperty("err.detail") + ":\n\n" + ex.getMessage(), 
                    language.getProperty("app.title"), 
                    JOptionPane.ERROR_MESSAGE);
            }
    }
  }
