    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:

        //pasa los datos de filtro a al PnlListadoAlumno
        int opcio=0;

        if(opFin.isSelected())opcio=1;
        if(opAbiertos.isSelected())opcio=2;

        String txt=txtData.getText();
        Date data=null;

        try {

            if(txt.equals(""))
            {
                data=null;
            }
            else
            {

                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                data = formato.parse(txt);
            }

            PnlListadoAlumno form = new PnlListadoAlumno(parent, true, manager, language, usuario, opcio,data);
            form.setLocationRelativeTo(null);
            form.setVisible(true);

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,
                language.getProperty("err.formatoData"),
                language.getProperty("app.title"),
                JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

